package com.class02;

public class VariableDecleration {

	public static void main(String[] args) {
		int num1 = 123;
		int num2 = 2323;
		int num3 = 434314;
		
		
		//declare variable first and then assign value
		int n1;
		int n2;
		int n3;
		
		n1 = 32;
		n2 = 43;
		n3 = 435;
		
		
		/////declare all variables together and then assign value
		int number1, number2, number3;
		
		number1 = 23;
		number2 = 434;
		number3 = 1234;
		
		boolean var = n1 > n2;
		System.out.println(var);
		
		boolean isSun = true;
		boolean isRainy;
		isRainy = isSun;
		System.out.println(isRainy);
		

	}

}
